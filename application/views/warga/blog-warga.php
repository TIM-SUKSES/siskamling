          </li>
          <li>
            Gunakan kunci berkualitas tinggi: Pastikan pintu dan jendela rumah Anda dilengkapi dengan kunci berkualitas tinggi, yang
            sulit untuk dipecahkan oleh maling.
          </li>
          <li>
            Beri tahu tetangga: Beri tahu tetangga Anda jika Anda akan pergi untuk waktu yang lama, sehingga mereka dapat membantu
            mengawasi rumah Anda dan melapor jika ada kejadian yang mencurigakan.
          </li>
          <li>
            Tidak menyimpan barang berharga di tempat yang mudah dilihat: Jangan menyimpan barang berharga di tempat yang mudah
            dilihat oleh orang luar. Sebaiknya, simpan barang-barang berharga Anda di tempat yang sulit dijangkau oleh maling.
          </li>
          <li>
            Menjaga pencahayaan yang cukup: Pasang lampu-lampu di sekitar rumah yang memberikan pencahayaan yang cukup di malam
            hari, sehingga maling tidak merasa nyaman untuk masuk ke dalam rumah Anda.
          </li>
          <li>
            Membuat kesan bahwa ada orang yang tinggal di dalam rumah: Jangan biarkan rumah Anda terlihat kosong. Misalnya dengan
            membiarkan lampu di dalam rumah tetap menyala atau dengan menempatkan mainan di halaman rumah. Hal ini bisa memberi
            kesan bahwa rumah tersebut dihuni dan membuat maling enggan untuk masuk ke dalam rumah.
          </li>
        </ol>
        <p class="card-text text-light">Semoga tips-tips ini dapat membantu Anda untuk menjaga rumah Anda dari maling.</p>
